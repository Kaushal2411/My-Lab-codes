// Relational operators in c
#include <stdio.h>
int main () {
    int x,y;
    printf("Enter a number\n");
    scanf("%d",&x);
    printf("Enter another number\n");
    scanf("%d",&y);
    printf("%d\n",x>=y);
    printf("%d\n",x<=y);
    printf("%d\n",x>y);
    printf("%d\n",x<y);
    printf("%d\n",x==y);
    printf("%d\n",x!=y);
    return 0;
    
}